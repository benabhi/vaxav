<template>
  <div v-if="message" :class="alertClasses">
    {{ message }}
    <button
      v-if="dismissible"
      class="ml-auto pl-3"
      @click="dismiss"
      aria-label="Cerrar"
    >
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 20 20"
        fill="currentColor"
        class="w-5 h-5"
      >
        <path
          fill-rule="evenodd"
          d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z"
          clip-rule="evenodd"
        />
      </svg>
    </button>
  </div>
</template>

<script setup>
import { computed } from 'vue';

const props = defineProps({
  variant: {
    type: String,
    default: 'error',
    validator: (value) => ['success', 'error', 'warning', 'info'].includes(value)
  },
  message: {
    type: String,
    default: ''
  },
  dismissible: {
    type: Boolean,
    default: true
  }
});

const emit = defineEmits(['dismiss']);

const alertClasses = computed(() => {
  const baseClasses = 'p-3 rounded-md my-2 flex items-center';

  switch (props.variant) {
    case 'success':
      return `${baseClasses} bg-green-800 text-white`;
    case 'error':
      return `${baseClasses} bg-red-800 text-white`;
    case 'warning':
      return `${baseClasses} bg-yellow-800 text-white`;
    case 'info':
      return `${baseClasses} bg-blue-800 text-white`;
    default:
      return `${baseClasses} bg-red-800 text-white`;
  }
});

const dismiss = () => {
  emit('dismiss');
};
</script>
